#include "playlist.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main() {
    PlayList *playList = PlayListCreate();
    int f, option = -1;
    char musicName[100], artistName[100];
    unsigned int duration;

    printf("+---------------------------------------------------------------------------+\n");
    printf("+-------------------------------- BEM-VINDO --------------------------------+\n");
    printf("+---------------------------------------------------------------------------+\n");

    while(option != 0){
        printf("+------------------------------ MENU PRINCIPAL -----------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 1 - ADICIONAR MÚSICA -----------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 2 - REMOVER MÚSICA -------------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 3 - BUSCAR MÚSICA --------------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 4 - ORDENAR PLAYLIST -----------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 5 - SALVAR PLAYLIST ------------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 6 - ESTATÍSTICAS ---------------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 7 - TOCAR PLAYLIST -------------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 8 - MOSTRAR PLAYLIST -----------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 0 - SAIR -----------------------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ ESCOLHA UMA OPÇÃO: ");
        scanf("%d", &option);
        getchar(); 

        switch(option){
            case 1:
                int addOption = -1;
                printf("+---------------------------------------------------------------------------+\n");
                printf("+---------------------------- ADICIONAR MÚSICA -----------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ 1 - ADICIONAR MANUALMENTE ------------------------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ 2 - LER DO ARQUIVO 'musics.txt' ------------------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ INFORME A OPÇÃO: ");
                scanf("%d", &addOption);
                getchar();

                switch(addOption){
                    case 1:
                        printf("+---------------------------------------------------------------------------+\n");
                        printf("+ INFORME O NOME DA MÚSICA: ");
                        fgets(musicName, sizeof(musicName), stdin);
                        musicName[strcspn(musicName, "\n")] = 0;
                        printf("+---------------------------------------------------------------------------+\n");

                        printf("+ INFORME O ARTISTA: ");
                        fgets(artistName, sizeof(artistName), stdin);
                        artistName[strcspn(artistName, "\n")] = 0;
                        printf("+---------------------------------------------------------------------------+\n");


                        printf("+ INFORME A DURAÇÃO (SEGUNDOS): ");
                        scanf("%u", &duration);
                        getchar();
                        printf("+---------------------------------------------------------------------------+\n");

                        PlayListAdd(playList, artistName, musicName, duration, &f);
                        if(f == 1){
                            printf("+ MÚSICA ADICIONADA COM SUCESSO --------------------------------------------+\n");
                            printf("+---------------------------------------------------------------------------+\n");
                        }
                        
                        else{
                            printf("+ NÃO FOI POSSÍVEL ADICIONAR A MÚSICA À PLAYLIST ---------------------------+\n");
                            printf("+---------------------------------------------------------------------------+\n");
                        }
                                            
                        break;

                    case 2:
                        int h;
                        PlayListLoadFromFile(playList, "Musics.txt", &f, &h);
                        if(h == 1){
                            printf("+---------------------------------------------------------------------------+\n");
                            printf("+ MÚSICA(S) ADICIONADA COM SUCESSO -----------------------------------------+\n");
                            printf("+---------------------------------------------------------------------------+\n");
                        }

                        else{
                            printf("+---------------------------------------------------------------------------+\n");
                            printf("+ NÃO FOI POSSÍVEL ADICIONAR A(S) MÚSICA(S) À PLAYLIST ---------------------+\n");
                            printf("+---------------------------------------------------------------------------+\n");
                        }

                        break;
                                            
                    default:
                        printf("+---------------------------------------------------------------------------+\n");
                        printf("+ OPÇÃO INVÁLIDA! TENTE NOVAMENTE ------------------------------------------+\n");
                        printf("+---------------------------------------------------------------------------+\n");

                        break;
                }

                break;

            case 2:
                if(PlayListGetSize(playList) == 0){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ PLAYLIST VAZIA -----------------------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                
                    break;
                }
                printf("+---------------------------------------------------------------------------+\n");
                printf("+----------------------------- REMOVER MÚSICA ------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ INFORME O NOME DA MÚSICA PARA REMOVER: ");
                fgets(musicName, sizeof(musicName), stdin);
                musicName[strcspn(musicName, "\n")] = 0;

                PlayListRemove(playList, musicName, &f);
                if(f == 1){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ REMOÇÃO FINALIZADA -------------------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                }

                else{
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ NÃO FOI POSSÍVEL REMOVER A MÚSICA ----------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                }
            
                break;

            case 3:
                if(PlayListGetSize(playList) == 0){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ PLAYLIST VAZIA -----------------------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                
                    break;
                }
                printf("+---------------------------------------------------------------------------+\n");
                printf("+----------------------------- BUSCAR MÚSICA -------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ INFORME O NOME DA MÚSICA PARA BUSCAR: ");
                fgets(musicName, sizeof(musicName), stdin);
                musicName[strcspn(musicName, "\n")] = 0;
                Music *music = PlayListSearch(playList, musicName, &f);

                if(f == 1){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ A MÚSICA '%s' FOI ENCONTRANDA\n", musicName);
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ MÚSICA: %s | ARTISTA: %s | DURAÇÃO: %u\n", MusicGetMusicName(music), MusicGetArtistName(music), MusicGetDurationTime(music));
                    printf("+---------------------------------------------------------------------------+\n");
                }

                else{
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ A MÚSICA '%s' NÃO FOI ENCONTRANDA\n", musicName);
                    printf("+---------------------------------------------------------------------------+\n");
                }

                break;

            case 4:
                if(PlayListGetSize(playList) == 0){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ PLAYLIST VAZIA -----------------------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                
                    break;
                }
                printf("+---------------------------------------------------------------------------+\n");
                printf("+--------------------------- ORDENAR PLAYLIST ------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                PlayListSortByDuration(playList);
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ A PLAYLIST FOI ORDENADA --------------------------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");

                break;

            case 5:
                int f;
                if(PlayListGetSize(playList) == 0){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ PLAYLIST VAZIA -----------------------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                
                    break;
                }
                printf("+---------------------------------------------------------------------------+\n");
                printf("+----------------------------- SALVAR PLAYLIST -----------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                PlayListSaveToFile(playList, "My Playlist.txt", &f);
                if(f == 1){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ PLAYLIST SALVA COM SUCESSO -----------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                }

                else{
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ NÃO FOI POSSÍVEL SALVAR A PLAYLIST ---------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                }

                break;

            case 6:
                if(PlayListGetSize(playList) == 0){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ PLAYLIST VAZIA -----------------------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                
                    break;
                }
                printf("+---------------------------------------------------------------------------+\n");
                printf("+------------------------------ ESTATÍSTICAS -------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                PlayListStats(playList);

                break;

            case 7:
                if(PlayListGetSize(playList) == 0){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ PLAYLIST VAZIA -----------------------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");

                    break;
                }
                printf("+---------------------------------------------------------------------------+\n");
                printf("+------------------------------ TOCAR PLAYLIST -----------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ QUANTAS VEZES DESEJA TOCAR A PLAYLIST? ");
                unsigned int times;
                scanf("%u", &times);
                getchar();
                printf("+---------------------------------------------------------------------------+\n");
                PlayListplay(playList, times, &f);
                
                break;

            case 8:
                if(PlayListGetSize(playList) == 0){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ PLAYLIST VAZIA -----------------------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");

                    break;
                }
                printf("+---------------------------------------------------------------------------+\n");
                printf("+------------------------------ PLAYLIST ATUAL -----------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                PlayListPrint(playList);

                break;

            case 0:
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ SAINDO... ATÉ LOGO -------------------------------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");

                break;

            default:
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ OPÇÃO INVÁLIDA! TENTE NOVAMENTE ------------------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
        }
    }

    PlayListDestroy(playList, &f);

    return 0;
}